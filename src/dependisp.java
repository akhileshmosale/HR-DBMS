    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String depid = jTextField2.getText();
        int existing = 0;
        
        
        
        DefaultTableModel model;
            model=(DefaultTableModel)jTable1.getModel();
        try {
            Connection myConn = DriverManager.getConnection("jdbc:mysql://localhost:3306/mini", "root", "root");
            Statement myStat = myConn.createStatement();
            java.sql.ResultSet myRs = myStat.executeQuery("Select * from dependent");
            while (myRs.next()) {
                if (myRs.getString("DependentID").equals(depid)) {
                    existing++;
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
        if (existing == 0) {
            JOptionPane.showMessageDialog(null, "Record not found");
        } else {
            jLabel1.setEnabled(true);
            jButton1.setEnabled(true);
            jTable1.setEnabled(true);
            jLabel4.setEnabled(false);
            jTextField2.setEnabled(false);
            jButton4.setEnabled(false);
            try {
                Connection myConn = DriverManager.getConnection("jdbc:mysql://localhost:3306/mini", "root", "root");
                Statement myStat = myConn.createStatement();
                java.sql.ResultSet myRs = myStat.executeQuery("Select * from dependent where DependentID='"+depid+"'");
                 while(myRs.next())
               {
 
                  
                   model.addRow(new Object[]{myRs.getString(1),myRs.getString(2),myRs.getString(3),myRs.getString(4)});
               
               
               }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

